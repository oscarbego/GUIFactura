    public void mostarFactura(){
    
        
        jTextArea1.setText("");
        
        jTextArea1.append("------------------------------- \n");
        
        jTextArea1.append("Lista de Articulos de la Factura \n");
        
        jTextArea1.append("Cantidad | ");
        jTextArea1.append(" Nombre | ");
        jTextArea1.append(" Precio/U | ");
        jTextArea1.append(" Importe \n");
        jTextArea1.append("\n");
        
        for(int i = 0; i < factu.lista_pedidos.length; i++)
        {
            if(factu.lista_pedidos[i] != null)
            {
                jTextArea1.append(factu.lista_pedidos[i].getCanti() + "  ");
                jTextArea1.append(factu.lista_pedidos[i].getArticulo().nom + "  ");
                jTextArea1.append(factu.lista_pedidos[i].getArticulo().precio_u + "  ");
                jTextArea1.append(factu.lista_pedidos[i].getImporte() + "  \n");
            }
        }
        
        jTextArea1.append("\n");
        
        jTextArea1.append("SubTotal: " + factu.subTotal + "\n");        
        jTextArea1.append("IVA: " + factu.iva + "\n");
        jTextArea1.append("Total: " + factu.total + "\n");
        
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        factu = new Factura();
        
        mostarFactura();
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if(!jTextField1.getText().equals("")){
            
            Articulo arti = new Articulo();
            arti.nom = "Chocolates";
            arti.precio_u = 10;
            
            int canti = Integer.parseInt(jTextField1.getText());
            
            factu.agregarPedido(canti, arti);
            
            mostarFactura();
        }
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

